    def _detect_language(self, text: str) -> str:
        """检测文本语言"""
        lang_scores = {}
        
        for lang, pattern in self.patterns.items():
            matches = re.findall(pattern, text)
            lang_scores[lang] = len(matches)
        
        if not lang_scores or all(v == 0 for v in lang_scores.values()):
            return 'en'
        
        max_score = max(lang_scores.values())
        if max_score == 0:
            return 'en'
        
        # 处理中日韩混合情况
        if lang_scores['zh'] > 0 and lang_scores['ja'] > 0:
            # 检查片假名比例来区分中日
            katakana = re.findall(r'[\u30a0-\u30ff]', text)
            if len(katakana) > lang_scores['zh'] * 0.1:
                return 'ja'
            return 'zh'
        
        # 返回得分最高的语言
        detected_lang = max(lang_scores, key=lang_scores.get)
        
        # 确认检测结果
        confidence = lang_scores[detected_lang] / len(text) if len(text) > 0 else 0
        if confidence < 0.3:
            return 'en'
        
        return detected_lang

    def translate(
        self,
        text: str,
        target_language: str,
        source_language: Optional[str] = None,
        quality: str = "standard",
        preserve_formatting: bool = True
    ) -> TranslationResult:
        """翻译文本"""
        if not text or not text.strip():
            return TranslationResult(
                original_text="",
                translated_text="",
                source_language="",
                target_language=target_language,
                confidence=0.0
            )

        source_lang = source_language or self._detect_language(text)
        
        if source_lang == target_language:
            return TranslationResult(
                original_text=text,
                translated_text=text,
                source_language=source_lang,
                target_language=target_language,
                confidence=1.0,
                word_count=len(text.split()),
                char_count=len(text),
                preserved_formatting=True
            )

        translated_text = self._perform_translation(text, source_lang, target_language, quality)
        suggestions = self._generate_suggestions(text, translated_text, source_lang, target_language)

        return TranslationResult(
            original_text=text,
            translated_text=translated_text,
            source_language=source_lang,
            target_language=target_language,
            confidence=self._calculate_confidence(text, source_lang, target_language),
            word_count=len(translated_text.split()),
            char_count=len(translated_text),
            suggestions=suggestions,
            preserved_formatting=preserve_formatting
        )

    def _perform_translation(
        self,
        text: str,
        source_lang: str,
        target_lang: str,
        quality: str
    ) -> str:
        """执行翻译"""
        key = f"{source_lang}_to_{target_lang}"
        
        if key in self.translation_rules:
            return self.translation_rules[key](text)
        
        return self._generic_translate(text, source_lang, target_lang)

    def _generic_translate(self, text: str, source_lang: str, target_lang: str) -> str:
        """通用翻译回退"""
        lang_names = {
            'zh': '中文', 'en': 'English', 'ja': '日本語', 'ko': '한국어',
            'fr': 'Français', 'de': 'Deutsch', 'es': 'Español', 'ru': 'Русский'
        }
        
        source_name = lang_names.get(source_lang, source_lang)
        target_name = lang_names.get(target_lang, target_lang)
        
        return f"[Translated from {source_name} to {target_name}]: {text}"

